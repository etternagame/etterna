function getRandomQuote(tipType)
    if tipType == 1 then
        if #Tips > 0 then
            return "TIP: "..Tips[math.random(#Tips)]
        else
            return "ERROR: No tips"
        end
    elseif tipType == 2 then
        if #Phrases > 0 then
            return Phrases[math.random(#Phrases)]
        else
            return "ERROR: No quotes"
        end
    else
        return "ERROR: bruh how did u break this ??????"
    end
end
